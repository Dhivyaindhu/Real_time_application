import streamlit as st
import requests
import re
from typing import Optional, Tuple
from PIL import Image

# OCR Class
class SimpleBillOCR:
    def __init__(self, api_key: str):
        self.api_key = api_key

    def extract_text(self, image_bytes: bytes) -> Optional[str]:
        """Extract text from image using OCR.space API"""
        try:
            url = 'https://api.ocr.space/parse/image'
            response = requests.post(
                url,
                files={'file': image_bytes},
                data={
                    'apikey': self.api_key,
                    'language': 'eng',
                    'detectOrientation': 'true',
                    'scale': 'true',
                    'OCREngine': '2'
                },
                timeout=30
            )
            result = response.json()

            if result.get('IsErroredOnProcessing'):
                return None

            return result['ParsedResults'][0]['ParsedText']

        except Exception as e:
            return None

    def find_total(self, text: str) -> Tuple[Optional[float], Optional[str]]:
        """Find the total amount in the extracted text"""
        if not text:
            return None, None

        lines = [line.strip() for line in text.split('\n') if line.strip()]
        patterns = [
            r'(?:total|amount|sum)\s*[:.]?\s*[\$]?\s*([\d,]+(?:\.\d{0,2})?)',
            r'(?:grand|final|sub)?\s*total\s*[:.]?\s*([\d,]+(?:\.\d{0,2})?)'
        ]

        for line in lines:
            for pattern in patterns:
                match = re.search(pattern, line.lower())
                if match:
                    amount_str = match.group(1)
                    try:
                        amount = float(amount_str.replace(',', ''))
                        return amount, line
                    except ValueError:
                        continue

        return None, None


